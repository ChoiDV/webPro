-- FRIEND TABLE & SEQ DROP & CREATE 
DROP TABLE FRIEND;
DROP SEQUENCE FRIEND_SEQ;
CREATE SEQUENCE FRIEND_SEQ MAXVALUE 9999;
CREATE TABLE FRIEND(
    NO NUMBER(4,0) PRIMARY KEY,
    NAME VARCHAR2(30) ,
    TEL VARCHAR2(30)
);
